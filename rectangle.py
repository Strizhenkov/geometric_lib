def area(a, b):
    """
    Принимает длину a и ширину b прямоугольника, возвращает его площадь

    :param a: (int/float) длина прямоугольника
    :param b: (int/float) ширина прямоугольника
    :return: (int/float) площадь прямоугольника
    """
    return a * b


def perimeter(a, b):
    """
    Принимает длину a и ширину b прямоугольника, возвращает его периметр

    :param a: (int/float) длина прямоугольника
    :param b: (int/float) ширина прямоугольника
    :return: (int/float) периметр прямоугольника
    """
    return 2 * (a + b)
